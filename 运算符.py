# 算术运算符 + - * / %(求余数) **(求幂) //(求商的整数)
print(1 + 3)
print(1.0 + 3)
print(1 * 3)
print(1 - 3)
print(1 / 3)
print(1 % 3)
print(1 // 3)
print(2 ** 3)

